package ec.company_grade;

public class cgVo {
	private int cg_id;
	private String cg_name;
	private String cg_mil_rate;
	private String cg_reg;
	
	public int getCg_id() {
		return cg_id;
	}
	public void setCg_id(int cg_id) {
		this.cg_id = cg_id;
	}
	public String getCg_name() {
		return cg_name;
	}
	public void setCg_name(String cg_name) {
		this.cg_name = cg_name;
	}
	public String getCg_mil_rate() {
		return cg_mil_rate;
	}
	public void setCg_mil_rate(String cg_mil_rate) {
		this.cg_mil_rate = cg_mil_rate;
	}
	public String getCg_reg() {
		return cg_reg;
	}
	public void setCg_reg(String cg_reg) {
		this.cg_reg = cg_reg;
	}
}
