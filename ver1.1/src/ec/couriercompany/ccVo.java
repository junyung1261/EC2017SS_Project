package ec.couriercompany;

public class ccVo {
	private int cc_id;
	private String cc_name;
	private String cc_reg;
	
	public int getCc_id() {
		return cc_id;
	}
	public void setCc_id(int cc_id) {
		this.cc_id = cc_id;
	}
	public String getCc_name() {
		return cc_name;
	}
	public void setCc_name(String cc_name) {
		this.cc_name = cc_name;
	}
	public String getCc_reg() {
		return cc_reg;
	}
	public void setCc_reg(String cc_reg) {
		this.cc_reg = cc_reg;
	}
	
}
