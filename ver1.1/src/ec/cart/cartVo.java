package ec.cart;

public class cartVo {

	
	private int mem_id;
	private int pdd_id;
	private int cart_count;
	private String cart_datetime;
	public int getMem_id() {
		return mem_id;
	}
	public void setMem_id(int mem_id) {
		this.mem_id = mem_id;
	}
	public int getPdd_id() {
		return pdd_id;
	}
	public void setPdd_id(int pdd_id) {
		this.pdd_id = pdd_id;
	}
	public int getCart_count() {
		return cart_count;
	}
	public void setCart_count(int cart_count) {
		this.cart_count = cart_count;
	}
	public String getCart_datetime() {
		return cart_datetime;
	}
	public void setCart_datetime(String cart_datetime) {
		this.cart_datetime = cart_datetime;
	}
	
	
}
