package ec.fee;

public class feeVo {
	private int f_id;
	private float f_rate;
	private String f_reg;
	
	public int getF_id() {
		return f_id;
	}
	public void setF_id(int f_id) {
		this.f_id = f_id;
	}
	public float getF_rate() {
		return f_rate;
	}
	public void setF_rate(float f_rate) {
		this.f_rate = f_rate;
	}
	public String getF_reg() {
		return f_reg;
	}
	public void setF_reg(String f_reg) {
		this.f_reg = f_reg;
	}
}
